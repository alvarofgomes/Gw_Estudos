public class Carro {

	private String modelo;
	private int ano;
	private double precos;
	
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public double getPrecos() {
		return precos;
	}
	public void setPrecos(double precos) {
		this.precos = precos;
	}
	
	
}